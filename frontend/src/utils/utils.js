export const convertNameEngToFr = (name) => {
    switch (name) {
        case 'Sports':
            return "Sport"
        case 'Automotive':
            return "Automobile"
        case 'Geography':
            return "Géographie"
        case 'Music':
            return "Musique"
        case 'Animals':
            return "Animaux"
        case 'Video_games':
            return "Jeu vidéo"
        case 'General_Culture':
            return "Culture générale"
        case 'Science':
            return "Sciences"
        case 'Movies':
            return "Cinéma"
        case 'Computer_science':
            return "Informatique"
        case 'Medicine':
            return "Médecine"
        case 'Literature':
            return "Littérature"
    }
}


